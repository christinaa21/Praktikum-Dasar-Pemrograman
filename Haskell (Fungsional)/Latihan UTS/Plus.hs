module Plus where
    -- PLUS                                     plus (x,y)
    -- DEFINISI DAN SPESIFIKASI
    plus :: Int -> Int -> Int 
    {- Fungsi plus(x,y) menjumlahkan dua bilangan integer. -}
    -- REALISASI
    plus x y = x + y
    -- CONTOH APLIKASI
    -- plus 1 1
    -- 2
    -- plus 2 4
    -- 6
